package classes;

public class Entry {
    public String name;
    public double value;
    public int day;
    public int month;
    public int year;
    public Entry(String name, double value, int day, int month, int year){
        this.name=name;
        this.value=value;
        this.day=day;
        this.month=month;
        this.year=year;
    }
}
